void App_startUp()
{
    OhiGroveOled112_clearDisplay(&oled);
//    OhiGroveOled112_setNormalDisplay(&oled);
    OhiGroveOled112_setInverseDisplay(&oled);
    OhiGroveOled112_setHorizontalMode(&oled);
    OhiGroveOled112_drawBitmap(&oled,App_ohilabLogo,1024);
    Timer_delay(3000);
    OhiGroveOled112_clearDisplay(&oled);
    Timer_delay(300);
    OhiGroveOled112_drawBitmap(&oled,App_openLogo,1024);
    Timer_delay(3000);
    OhiGroveOled112_clearDisplay(&oled);
    Timer_delay(300);
    OhiGroveOled112_setTextXY(&oled,0,0);          //Set the cursor to Xth Page, Yth Column
    OhiGroveOled112_putString(&oled,PROJECT_NAME); //Print the String
    OhiGroveOled112_setTextXY(&oled,2,0);          //Set the cursor to Xth Page, Yth Column
    OhiGroveOled112_putString(&oled,PROJECT_COPYRIGTH); //Print the String
    OhiGroveOled112_setTextXY(&oled,4,0);          //Set the cursor to Xth Page, Yth Column
    OhiGroveOled112_putString(&oled,"Board Version: "); //Print the String
    OhiGroveOled112_setTextXY(&oled,5,0);          //Set the cursor to Xth Page, Yth Column
    OhiGroveOled112_putString(&oled,PCB_VERSION_STRING); //Print the String
    OhiGroveOled112_setTextXY(&oled,6,0);          //Set the cursor to Xth Page, Yth Column
    OhiGroveOled112_putString(&oled,"Firmware Version: "); //Print the String
    OhiGroveOled112_setTextXY(&oled,7,0);          //Set the cursor to Xth Page, Yth Column
    OhiGroveOled112_putString(&oled,FW_VERSION_STRING); //Print the String
    Timer_delay(5000);
    OhiGroveOled112_clearDisplay(&oled);
    Timer_delay(300);

}
